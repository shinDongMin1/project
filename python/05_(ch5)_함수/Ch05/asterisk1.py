# 실습 1 - args의 정체가 무엇인지 알아보자
# -----------------------------------------------------------------------------------------------------------

def asterisk_test(a, b, *args):
    print(type(args), args)
    return a + b + sum(args)

print(asterisk_test(1, 2, 3, 4, 5))
# <class 'tuple'> (3, 4, 5)
# 15

exit(0)